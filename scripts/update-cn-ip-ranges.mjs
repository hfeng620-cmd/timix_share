import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

import ipaddr from "ipaddr.js";

const sources = {
  ipv4: "https://www.ipdeny.com/ipblocks/data/aggregated/cn-aggregated.zone",
  ipv6: "https://www.ipdeny.com/ipv6/ipaddresses/aggregated/cn-aggregated.zone",
};

function addressValue(address) {
  return address
    .toByteArray()
    .reduce((value, byte) => (value << 8n) | BigInt(byte), 0n);
}

function cidrRange(cidr) {
  const [address, prefix] = ipaddr.parseCIDR(cidr);
  const bits = address.kind() === "ipv4" ? 32 : 128;
  const hostBits = BigInt(bits - prefix);
  const size = 1n << hostBits;
  const start = (addressValue(address) >> hostBits) << hostBits;
  return [start, start + size - 1n];
}

function mergeRanges(ranges) {
  const sorted = ranges.sort((left, right) =>
    left[0] < right[0] ? -1 : left[0] > right[0] ? 1 : 0,
  );
  const merged = [];

  for (const range of sorted) {
    const previous = merged.at(-1);
    if (previous && range[0] <= previous[1] + 1n) {
      if (range[1] > previous[1]) previous[1] = range[1];
    } else {
      merged.push([...range]);
    }
  }

  return merged;
}

async function downloadRanges(url, expectedKind) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to download ${url}: HTTP ${response.status}`);
  }

  const cidrs = (await response.text())
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean);

  if (cidrs.length < 100) {
    throw new Error(`Refusing unexpectedly small ${expectedKind} list`);
  }

  for (const cidr of cidrs) {
    const [address] = ipaddr.parseCIDR(cidr);
    if (address.kind() !== expectedKind) {
      throw new Error(`Unexpected ${address.kind()} range in ${expectedKind} list`);
    }
  }

  return mergeRanges(cidrs.map(cidrRange));
}

function serialize(name, ranges) {
  const rows = ranges
    .map(([start, end]) => `${start.toString(16)}-${end.toString(16)}`)
    .join("\n");
  return `export const ${name} = \`\n${rows}\n\`;`;
}

const [ipv4Ranges, ipv6Ranges] = await Promise.all([
  downloadRanges(sources.ipv4, "ipv4"),
  downloadRanges(sources.ipv6, "ipv6"),
]);

const outputPath = resolve("src/generated/cn-ip-ranges.ts");
const output = `// Generated by scripts/update-cn-ip-ranges.mjs from IPdeny aggregated CN ranges.\n// Do not edit this file manually.\n\n${serialize("CN_IPV4_RANGES_HEX", ipv4Ranges)}\n\n${serialize("CN_IPV6_RANGES_HEX", ipv6Ranges)}\n`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, output, "utf8");
console.log(
  `[cn-ip-ranges] wrote ${ipv4Ranges.length} IPv4 and ${ipv6Ranges.length} IPv6 ranges`,
);
